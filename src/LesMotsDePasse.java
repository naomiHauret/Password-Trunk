import java.io.Serializable;
import java.util.Vector;

public class LesMotsDePasse implements Serializable{
	
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//VARIABLES
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	private Vector<String> site;
	private Vector<String> id;
	private Vector<String> mdp;
	
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//CONSTRUCTEUR
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	public LesMotsDePasse(){
		this.site= new Vector();
		this.id= new Vector();
		this.mdp= new Vector();
	}
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	//METHODES
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	public void ajoutMotDePasse(String nomSite, String lId, String leMdp){
		this.site.add(nomSite);
		this.id.add(lId);
		this.mdp.add(leMdp);
	}
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	
	public void retireMotDePasse(int index){
		this.site.remove(index);
		this.id.remove(index);
		this.mdp.remove(index);
	}
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	public int size(){
		int res=-1;
		if(this.site.size()==this.id.size() && this.id.size()==this.mdp.size()){
				res= this.site.size();
		}
		
		return res;
	}
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

	public String[] getLeMdpAtIndex(int i){
		String[] leMdpEntier = {this.site.get(i), this.id.get(i), this.mdp.get(i)};	
		
		return leMdpEntier;	
	}
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
	public void modifierMotDePasse(int i, String[] t){
		this.site.setElementAt(t[0],i);
		this.id.setElementAt(t[1],i);
		this.mdp.setElementAt(t[2], i);	
	}
		
	//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
}

